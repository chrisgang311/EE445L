// alarm and clock time values
// 32-bit packed time value. top 16bits are hour, bottom 16 are minute.
// alt: 24 bit-packed time value with 8 bit sections: (0xhhmmss)
static uint32_t ClockTime, AlarmTime;

// true if alarm is on.
static bool power = false;

/** Clock_Init() **
 * Initialize the alarm clock (Plug in the alarm clock)
 * Must be called first
 */
void Clock_Init(void){
	ClockTime = AlarmTime = 0;
	PLL_Init(Bus80MHz);
	Timer0A_Init(TIMER_1Hz / 16);
}

/** Clock_Reset() **
 * Reset the clock time.
 */
 void Clock_Reset(void){
	ClockTime = 0;
}

/** Clock_Reset() **
 * Reset the clock time.
 */
 void Clock_Increment(void){
	uint32_t time = ClockTime;
	uint32_t minutes, hours, seconds;
	hours = (time & 0xFF0000) >> 16;
	minutes = (time & 0x00FF00) >> 8;
	seconds = (time & 0x0000FF) >> 0;
	
	// increment clock
	seconds = seconds + 1;
	if(seconds >= 60){
		seconds = 0;
		minutes = minutes + 1;
		if(minutes >= 60){
			minutes = 0;
			hours = hours + 1;
			if(hours >= 24){
				hours = 0;
			}
		}
	}
	hours = (hours << 16);
	minutes = (minutes << 8);
	ClockTime = hours + minutes + seconds;
}

/** Clock_Read() **
 * Read the clock time.
 */
uint32_t Clock_Read(void){
	return ClockTime;
}

/** Clock_Set() **
 * Set the current clock time.
 */
void Clock_Set(uint32_t time){
	ClockTime = time;
}

/** Alarm_Set() ** 
 * Set the alarm
 */
void Alarm_Set(uint32_t time){
	AlarmTime = time;
}

/** Alarm_Read() ** 
 * Read the alarm value.
 */
uint32_t Alarm_Read(void){
	return AlarmTime;
}

/** Alarm_TurnedOn() ** 
 * Is the alarm on?
 */
bool Alarm_Ready(){
	return power;
}

/** Alarm_PowerOn() ** 
 * Turn on the alarm
 */
void Alarm_PowerOn(void){
	power = true;
}

/** Alarm_PowerOff() ** 
 * Turn off the alarm
 */
void Alarm_PowerOff(void){
	power = false;
}

