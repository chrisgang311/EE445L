// clock hand math
static float sine(float angle);
static float cosine(float angle);

/** Clock_DrawHourHand() **
 * Plots the hours hand of the clock.
 * uses floating point trigonometry.
 */
void Clock_DrawHourHand(uint8_t hours, uint16_t color){
	const float PI = 3.1415926535897932384626;
	const uint8_t ORIGIN_X = LCD_WIDTH / 2 ; 
	const uint8_t ORIGIN_Y = LCD_HEIGHT / 2; 
	const uint8_t RADIUS = CLOCK_WIDTH * 3 / 16; 
	
	// translate absolute angle
	hours = hours % 12; 
	float angle = (hours * PI) / 6; 
	if((hours >= 3 && hours < 6) || (hours >= 9 && hours < 12)){
		angle = (PI) - angle; // flip angle
	}

	// plot hand (x,y)
	uint8_t handX = 0, handY = 0; 
	if(hours < 3){ // quadrant 1
		handX = (ORIGIN_X + (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y - (RADIUS * cosine(angle)));
	}
	// quadrant 4 (3-6 o'clock)
	else if(hours >= 3 && hours < 6){ 
		handX = (ORIGIN_X + (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y + (RADIUS * cosine(angle)));
	} 
	// quadrant 3 (6-9 o'clock)
	else if(hours >= 6 && hours < 9){
		handX = (ORIGIN_X + (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y - (RADIUS * cosine(angle)));
	} 
	else{ // quadrant 2 (9-12 o'clock)
		handX = (ORIGIN_X + (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y + (RADIUS * cosine(angle)));
	} LCD_DrawLine(ORIGIN_X, ORIGIN_Y, handX, handY, color);
}  

/** Clock_DrawMinuteHand() **
 * Plots the minutes hand of the clock.
 * uses floating point trigonometry.
 */
void Clock_DrawMinuteHand(uint8_t minutes, uint16_t color){
	const float PI = 3.14159265359;
	const uint8_t ORIGIN_X = LCD_WIDTH / 2 ; 
	const uint8_t ORIGIN_Y = LCD_HEIGHT / 2; 
	const uint8_t RADIUS = CLOCK_WIDTH * 5 / 16; 

	// translate absolute angle
	float angle = ((minutes % 15) * PI / 30);
	if((minutes >= 15 && minutes < 30) || (minutes >= 45 && minutes < 60)){
		angle = (PI / 2) - angle; // flip angle
	}

	// plot hand (x,y)
	uint8_t handX = 0, handY = 0; 
	if(minutes <= 15){ // quadrant 1
		handX = (ORIGIN_X + (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y - (RADIUS * cosine(angle)));
	}
	// quadrant 4 (15-30 minutes)
	else if(minutes > 15 && minutes <= 30){ 
		handX = (ORIGIN_X + (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y + (RADIUS * cosine(angle)));
	} 
	// quadrant 3 (30-45 minutes)
	else if(minutes > 30 && minutes <= 45){
		handX = (ORIGIN_X - (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y + (RADIUS * cosine(angle)));
	} 
	else{ // quadrant 2 (45-60 minutes)
		handX = (ORIGIN_X - (RADIUS * sine(angle))); 
		handY = (ORIGIN_Y - (RADIUS * cosine(angle)));
	} LCD_DrawLine(ORIGIN_X, ORIGIN_Y, handX, handY, color);
}

